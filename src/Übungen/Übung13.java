package Übungen;
public class Übung13 {
    
    public static void main(String[] args) {
        
        double w = 5.6;
        double h = 8.5;

        double result_area = w * h;
        double result_perimeter = 2 * (w + h);

        System.out.println("Area is " + w + " * " + h + " = " + result_area);
        System.out.println("Perimeter is " + 2 + " * " + "(5.6 + 8.5)" + " = " + result_perimeter);
    }


}
